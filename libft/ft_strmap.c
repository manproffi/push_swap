#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*str;
	size_t	len;
	size_t	i;

	i = 0;
	if (s && f)
	{
		len = ft_strlen(s);
		str = (char*)malloc(sizeof(char) * len + 1);
		if (str == NULL)
			return (NULL);
		while (i < len)
		{
			str[i] = (*f)(s[i]);
			i++;
		}
		str[i] = '\0';
		return (str);
	}
	return (NULL);
}
