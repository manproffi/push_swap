#include "stdlib.h"

int		ft_isalpha(int c)
{
	unsigned char	s;

	s = (unsigned char)c;
	if (c > 64 && c < 123)
	{
		if ((s > 96 && s < 123) || (s > 64 && s < 91))
			return (1);
		return (0);
	}
	return (0);
}
